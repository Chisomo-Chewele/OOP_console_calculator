#include "Arithmetic.h"
int Arithmetic::add(int a, int b)
{
	int sum;
	sum = (a + b);
	return sum;
}

int Arithmetic::sub(int a, int b)
{
	int difference;
	difference = (a - b);
	return difference;
}

int Arithmetic::multi(int a, int b)
{
	int product;
	product = (a * b);
	return product;
}

double Arithmetic::div(double a, double b)
{
	double dividend;
	dividend = (a / b);
	return dividend;
}